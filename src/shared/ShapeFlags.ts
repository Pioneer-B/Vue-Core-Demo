/**
 * | (两位都为0，才为0)
 * 修改 |
 * 0000 | 0001 = 0001
 * 0001 | 0100 = 0101
 *
 * &（两位都为1，才为1）
 * 0001 & 0001 = 0001
 * 0010 & 0011 = 0010
 */
export const enum ShapeFlags {
  // 1 << 0 = 1 → 二进制 0001
  ELEMENT = 1,
  // 1 << 1 = 2 → 二进制 0010
  STATEFUL_COMPONENT = 1 << 1,
  // 1 << 2 = 4 → 二进制 0100
  TEXT_CHILDREN = 1 << 2,
  // 1 << 3 = 8 → 二进制 1000
  ARRAY_CHILDREN = 1 << 3,
}
